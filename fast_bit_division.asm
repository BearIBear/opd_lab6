DIVIDE_BY: WORD 10
DIVISIBLE_RESULT: WORD 10000
LEFT: WORD ?
ITER: WORD 8

JUMP $START
DIVISION:
	LD $DIVISIBLE_RESULT
	ASL
	ST $DIVISIBLE_RESULT
	LD $LEFT
	ROL
	SUB $DIVIDE_BY
	BMI ADD_ZERO
	ADD_ONE:
		ST $LEFT
		LD $DIVISIBLE_RESULT
		INC
		ST $DIVISIBLE_RESULT
		JUMP $START
	ADD_ZERO:
		ADD $DIVIDE_BY
		ST $LEFT
	START: LOOP $ITER
	JUMP $DIVISION
HLT