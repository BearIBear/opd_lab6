ORG 0
WORD 0x30, 0x31, 0x32, 0x33, 0x34, 0x35, 0x36, 0x37, 0x38, 0x39 ; 0-9
WORD 2D ; -
WORD 2B ; +
WORD 2F ; /
WORD 2A ; *
WORD 2E ; .
WORD 3D ; =
CHAR: WORD ?
NUM: WORD ?

AWAIT_INPUT:
	IN 0x1D
	AND #0x40
	BEQ AWAIT_INPUT
IN 0x1C
ST $CHAR
CMP #0xA
BPL ISNT_NUMBER
	LD $NUM
	ASL
	ASL
	ASL
	ADD $NUM
	ADD $NUM
	ADD $CHAR
	ST $NUM
ISNT_NUMBER:
LD (CHAR)
OUT 0xC
JUMP $AWAIT_INPUT
HLT